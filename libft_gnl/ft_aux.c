#include "libft.h"

void	ft_write_ms(int i, char c)
{
	while (i > 0)
	{
		write(1, &c, 1);
		i--;
	}
}

void	ft_init_split3(t_split3 *t)
{
	t->i = 0;
	t->aux = 1;
	t->flag_cs = 0;
	t->flag_cd = 0;
}
