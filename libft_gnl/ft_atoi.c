#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	unsigned int	num;
	int				signo;

	i = 0;
	num = 0;
	signo = 1;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			signo = -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		num = (num * 10) + (str[i] - '0');
		i++;
	}
	return ((int)num * signo);
}
